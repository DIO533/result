    10,    0,    21.5;
    20,    21.5, 21.5;
    8,     21.5, 0;
    10,    0,    0;
    12,    0,    35.4;
    30,    35.4, 35.4;
    10,    35.4, 0;
    5,     0,    0;
    15,    0,    29.0;
    35,    29.0, 29.0;
    10,    29.0, 0;
    8,     0,    0;
    20,    0,    37.4;
    25,    37.4, 37.4;
    18,    37.4, 0;
    11,    0,    0;
    20,    0,    39.3;
    42,    39.3, 39.3;
    12,    39.3, 0;
    12,    0,    0;
    15,    0,    46.5;
    25,    46.5, 46.5;
    12,    46.5, 0;
    15,    0,    0;
    18,    0,    56.5;
    40,    56.5, 56.5;
    14,    56.5, 0;
    25,    0,    0;
];

% =============================================
% Phase 2: 中速（Medium） 433s，最大速度 76.6 km/h
% =============================================
med_phases = [
    11,    0,    0;
    20,    0,    51.4;
    30,    51.4, 51.4;
    15,    51.4, 0;
    8,     0,    0;
    15,    0,    54.0;
    35,    54.0, 54.0;
    15,    54.0, 0;
    8,     0,    0;
    20,    0,    66.5;
    55,    66.5, 66.5;
    20,    66.5, 0;
    13,    0,    0;
    25,    0,    76.6;
