end

function v_smooth = smooth_velocity(v)
% 对速度序列进行轻微平滑，减少数值噪声
v_smooth = v;
for i = 2:length(v)-1
    v_smooth(i) = 0.25*v(i-1) + 0.5*v(i) + 0.25*v(i+1);
end
v_smooth = max(0, v_smooth);
end
