];

% 将各阶段拼接为连续时间-速度向量
[t1, v1] = phases_to_vec(low_phases,    0);
[t2, v2] = phases_to_vec(med_phases,    t1(end));
[t3, v3] = phases_to_vec(high_phases,   t2(end));
[t4, v4] = phases_to_vec(exhigh_phases, t3(end));

t_total = [t1, t2(2:end), t3(2:end), t4(2:end)];
