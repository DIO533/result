% 重采样为整秒
t_end = floor(t_total(end));
t     = (0:t_end)';
v     = interp1(t_total, v_total, t, 'linear') / 3.6;   % km/h -> m/s
v     = max(v, 0);

end

% ---- 辅助局部函数：将分段矩阵转换为时间-速度向量 ----
function [tv, vv] = phases_to_vec(phases, t_offset)
    tv = t_offset;
    vv = phases(1, 2);
    for k = 1:size(phases, 1)
        dur = phases(k, 1);
        v_e = phases(k, 3);
        t_new = tv(end) + (1:dur);
        v_new = linspace(vv(end), v_e, dur + 1);
        tv = [tv, t_new];
        vv = [vv, v_new(2:end)];
    end
end
